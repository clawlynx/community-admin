import mongoose, { Schema, model } from "mongoose";

const adminUserSchema = new Schema(
  {
    username: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const Admin = model("Admin", adminUserSchema);

export default Admin;
